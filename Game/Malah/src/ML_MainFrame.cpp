CMLMainFrame::CMLMainFrame() : AMainFrame()
{
}

bool CMLMainFrame::PreCreateWindow(LPCREATEFRMSTRUCT pWc)
{
	if (g_CmdParams.iFullScreen)
	{
	//	pWc->dwExStyle = WS_EX_TOPMOST;
		pWc->style = WS_POPUP;
	}
	else
		pWc->style = WS_POPUP | WS_CAPTION | WS_SYSMENU | WS_MINIMIZEBOX;

	pWc->lpszName = g_MLStrings.GetString(ST_GAMETITLE).GetBuffer();

	return true;
}

